export function findPublicResource(slug: string | undefined): PublicResourceArticle | undefined {
  return publicResourceArticles.find((article) => article.slug === slug);
}

export function articleUrl(article: PublicResourceArticle): string {
  return `/resources/${article.slug}`;
}

export function articleJsonLd(article: PublicResourceArticle) {
  const url = `${PUBLIC_SITE_ORIGIN}${articleUrl(article)}`;
  const blocks: Array<Record<string, unknown>> = [
    {
      "@context": "https://schema.org",
      "@type": "Article",
      headline: article.title,
      description: article.description,
      inLanguage: "ar-SA",
      datePublished: article.updatedAt,
      dateModified: article.updatedAt,
      mainEntityOfPage: url,
      author: {
        "@type": "Organization",
        name: PUBLIC_SITE_NAME,
        url: PUBLIC_SITE_ORIGIN,
      },
      publisher: {
        "@type": "Organization",
        name: PUBLIC_SITE_NAME,
        url: PUBLIC_SITE_ORIGIN,
      },
      keywords: article.keywords.join(", "),
    },
    {
      "@context": "https://schema.org",
      "@type": "BreadcrumbList",
      itemListElement: [
        {
          "@type": "ListItem",
          position: 1,
          name: "الرئيسية",
          item: PUBLIC_SITE_ORIGIN,
        },
        {
          "@type": "ListItem",
          position: 2,
          name: "مركز المعرفة",
          item: `${PUBLIC_SITE_ORIGIN}/resources`,
        },
        {
          "@type": "ListItem",
          position: 3,
          name: article.title,
          item: url,
        },
      ],
    },
  ];

  if (article.faqs?.length) {
    blocks.push({
      "@context": "https://schema.org",
      "@type": "FAQPage",
      mainEntity: article.faqs.map((faq) => ({
        "@type": "Question",
        name: faq.question,
        acceptedAnswer: {
          "@type": "Answer",
          text: faq.answer,
        },
      })),
    });
  }

  return blocks;
}

